                    <b class="heading">First motion pictures</b><br/><br/>
                    A screenshot of Roundhay Garden Scene by the French Louis Le Prince, the world's first film
                    A frame from Roundhay Garden Scene, the world's earliest surviving film produced using a motion picture camera, by Louis Le Prince, 1888
                    By the end of the 1880s, the introduction of lengths of celluloid photographic film and the invention of motion picture cameras, 
                    which could photograph a rapid sequence of images using only one lens, allowed action to be captured and stored on a single compact reel of film.
                    Movies were initially shown publicly to one person at a time through "peep show" devices such as the Electrotachyscope, Kinetoscope and the Mutoscope. 
                    Not much later, exhibitors managed to project films on large screens for theatre audiences.
                    The first public screenings of films at which admission was charged were made in 1895 by the American Woodville Latham and his sons, using films 
                    produced by their Eidoloscope company, and by the – arguably better known – French brothers Auguste and Louis Lumière with ten of their own productions.
                    <br/>
                    <br/>
                    <br/>
                    <b class="heading">Our Film Industry</b><br/><br/>
                    The making and showing of motion pictures became a source of profit almost as soon as the process was invented. Upon seeing how successful their 
                    new invention, and its product, was in their native France, the Lumières quickly set about touring the Continent to exhibit the first films privately 
                    to royalty and publicly to the masses. In each country, they would normally add new, local scenes to their catalogue and, quickly enough, found local 
                    entrepreneurs in the various countries of Europe to buy their equipment and photograph, export, import, and screen additional product commercially. 
                    The Oberammergau Passion Play of 1898 was the first commercial motion picture ever produced. Other pictures soon followed, and motion pictures became 
                    a separate industry that overshadowed the vaudeville world. Dedicated theaters and companies formed specifically to produce and distribute films, 
                    while motion picture actors became major celebrities and commanded huge fees for their performances. By 1917 Charlie Chaplin had a contract that called 
                    for an annual salary of one million dollars. From 1931 to 1956, film was also the only image storage and playback system for television programming 
                    until the introduction of videotape recorders.
                    In the United States, much of the film industry is centered around Hollywood, California. Other regional centers exist in many parts of the world, 
                    such as Mumbai-centered Bollywood, the Indian film industry's Hindi cinema which produces the largest number of films in the world. Though the 
                    expense involved in making films has led cinema production to concentrate under the auspices of movie studios, recent advances in affordable film making 
                    equipment have allowed independent film productions to flourish.
                    Profit is a key force in the industry, due to the costly and risky nature of filmmaking; many films have large cost overruns, an example being Kevin 
                    Costner's Waterworld. Yet many filmmakers strive to create works of lasting social significance. The Academy Awards (also known as "the Oscars") are 
                    the most prominent film awards in the United States, providing recognition each year to films, based on their artistic merits. There is also a large 
                    industry for educational and instructional films made in lieu of or in addition to lectures and texts. Revenue in the industry is sometimes volatile 
                    due to the reliance on blockbuster films released in movie theaters. The rise of alternative home entertainment has raised questions about the future 
                    of the cinema industry, and Hollywood employment has become less reliable, particularly for medium and low-budget films.
