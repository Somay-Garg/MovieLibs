                    <b class="heading">What are Movies?</b><br/><br/>
                    A film – also called a movie, motion picture, moving picture, or photoplay – is a work of visual art that simulates experiences and otherwise 
                    communicates ideas, stories, perceptions, feelings, beauty, or atmosphere through the use of moving images. These images are generally 
                    accompanied by sound and, more rarely, other sensory stimulations. The word "cinema", short for cinematography, is often used to refer to 
                    filmmaking and the film industry, and to the art form that is the result of it.
                    <br/>
